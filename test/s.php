    <h2>Key Features:</h2>
    <ul>
        <li>Task Management: Users can create multiple boards, each representing a different project or category.</li>
        <li>CSV Data Storage: Tasks are stored in CSV files, ensuring easy access and portability.</li>
        <li>Add and Remove Tasks: Users can add new tasks to the "To Do" column and remove tasks from the boards.</li>
        <li>Task Movement: Tasks can be moved between the "To Do" and "Done" columns to track progress.</li>
        <li>User Interface: The application features a user-friendly interface with intuitive controls.</li>
        <li>Board Management: Users can create and delete boards, along with their associated tasks.</li>
    </ul>
    <h2>Implementation Details:</h2>
